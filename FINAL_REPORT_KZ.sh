🎓 ТЕХНИЧЕСКИЕ ДЕТАЛИ:

detectLanguage() алгоритм:
  1. Подсчитывает казахские буквы: [әғіңөұүқ]
  2. Подсчитывает русские буквы: [ёйцчшщъьюя]
  3. Если КЗ буквы ≥ РУ буквы → язык = казахский
  4. Иначе → язык = русский

Определение сохраняется в: sessionLanguages: Map<sessionId, 'ru'|'kz'>
Использование в ответах: lang === 'kz' ? "КЗ текст" : "РУ текст"

